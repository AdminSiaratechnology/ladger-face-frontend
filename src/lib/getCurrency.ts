import axios from "axios";

export async function getCurrency(countryIso) {
  try {
    // Example: countryIso = "IN", "US", "GB", "AE"
    const url = `https://restcountries.com/v3.1/alpha/${countryIso}?fields=name,currencies`;
    const { data } = await axios.get(url);

    if (!data || !data.currencies) {
      return { error: "Currency not found" };
    }

    const currencyCode = Object.keys(data.currencies)[0];
    const currencyData = data.currencies[currencyCode];

    return {
      country: data.name.common,
      currencyCode,
      currencyName: currencyData.name,
      currencySymbol: currencyData.symbol || null,
    };
  } catch (err) {
    return { error: "Invalid ISO code or API error" };
  }
}

